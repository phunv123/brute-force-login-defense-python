def get_locale():
    """Get current locale from session or request"""
    # Check session first
    if 'locale' in session:
        return session['locale']

    # Check request args (for language switch)
    if request.args.get('lang'):
        lang = request.args.get('lang')
        if lang in TRANSLATIONS:
            session['locale'] = lang
            return lang

    # Check browser Accept-Language header
    accept_lang = request.headers.get('Accept-Language', '')
    if 'vi' in accept_lang.lower():
        session['locale'] = 'vi'
        return 'vi'

    # Default to Vietnamese
    return 'vi'


def set_locale(lang):
    """Set locale"""
    if lang in TRANSLATIONS:
        session['locale'] = lang
    return session.get('locale', 'vi')


def translate(key, locale=None):
    """Translate a key to the current locale"""
    if locale is None:
        locale = get_locale()

    return TRANSLATIONS.get(locale, {}).get(key, key)


def _(key):
    """Shorthand for translate function - use in templates"""
    return translate(key)